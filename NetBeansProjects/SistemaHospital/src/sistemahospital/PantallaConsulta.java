    private void jbbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbbuscarActionPerformed
        
        try {
            Connection con= DriverManager.getConnection("jdbc:mysql://localhost/sistemahospital","root","");
            PreparedStatement seleccionar=con.prepareStatement("SELECT * FROM pacientes");
            ResultSet consulta = seleccionar.executeQuery();
            
            do {                
                Informacion.append(consulta.getString(1));
                Informacion.append("    ");
                Informacion.append(consulta.getString(2));
                Informacion.append("    ");
                Informacion.append(consulta.getString(3));
                Informacion.append("    ");
                Informacion.append(consulta.getString(4));
                Informacion.append("    ");
                Informacion.append(consulta.getString(5));
                Informacion.append("\n");
            } while (consulta.next());
        } catch (SQLException ex) {
            Logger.getLogger(PantallaConsulta.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null , "Si aparece esto es por que fallo la busqueda");
        }
    }//GEN-LAST:event_jbbuscarActionPerformed


